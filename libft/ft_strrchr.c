#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	x;

	if (!s)
		x = 0;
	else
		x = (int)ft_strlen(s);
	while (x >= 0)
	{
		if (s[x] == (char)c)
			return ((char *)&s[x]);
		x--;
	}
	return (NULL);
}
